import numpy as np
import matplotlib.pyplot as plt

# create 1-D arrays for [x1, x2] and an interval between -1 and 1
x1 = (np.random.rand(1200) - 0.5) * 2
x2 = (np.random.rand(1200) - 0.5) * 2

# Make an array for color picking
array_color = np.zeros(1200)

# XOR condition
for i in range(len(array_color)):
  if (x1[i] <= 0 and x2[i] > 0) or (x1[i] > 0 and x2[i] <= 0):
    array_color[i] = 1
  else:
    array_color[i] = 0

plt.xlabel('x1')
plt.ylabel('x2')
plt.title('XOR Scatter Plot')
plt.scatter(x1, x2, c=array_color);
